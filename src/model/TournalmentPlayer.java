package model;

import java.io.Serializable;

/**
 *
 * @author MSI GF63
 */
public class TournalmentPlayer implements Serializable{
    private int id;
    private Player player;
    private String note;

    public TournalmentPlayer() {
    }

    public TournalmentPlayer(int id, Player player, String note) {
        this.id = id;
        this.player = player;
        this.note = note;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Player getPlayer() {
        return player;
    }

    public void setPlayer(Player player) {
        this.player = player;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
    
    
    
}
